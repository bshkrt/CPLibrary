Mint fastp(Mint a, ll p) {
    Mint ans = 1;
    while (p) {
        if (p & 1)
            ans = ans * a;
        a = a * a;
        p >>= 1;
    }
    return ans;
}


Mint fac[N];
Mint invfac[N];
Mint invnum[N];

void init() {
    int n = N - 1;
    fac[0] = invnum[0] = invfac[0] = 1;
    for (int i = 1; i <= n; ++i) {
        fac[i] = fac[i - 1] * i;
    }
    invfac[n] = fastp(fac[n], mod - 2);
    invnum[n] = invfac[n] * fac[n - 1];
    for (int i = n - 1; i > 0; --i) {
        invfac[i] = invfac[i + 1] * (i + 1);
        invnum[i] = invfac[i] * fac[i - 1];
    }
}

Mint inv(Mint a) {
    if (a < N) return invnum[a];
    return fastp(a, mod - 2);
}

Mint cnk(int n, int k) {
    if (k < 0 || k > n) return 0;
    return fac[n] * invfac[n - k] % mod * invfac[k] % mod;
}


int add(int a, int b) {
    return a + b >= mod ? a + b - mod : a + b;
}

int sub(int a, int b) {
    return a - b >= 0 ? a - b : a - b + mod;
}

int mul(int a, int b) {
    return a * (ll) b % mod;
}


int fastp(int a, int b) {
    int ans = 1;
    while (b) {
        if (b & 1) ans = mul(ans, a);
        a = mul(a, a);
        b >>= 1;
    }
    return ans;
}


struct node {
    int sum;
    int mxp, mxs;
    int mnp, mns;

    node(int x = 0) : sum(x), mxp(std::max(0, x)), mxs(std::max(0, x)), mnp(std::min(0, x)), mns(std::min(0, x)) {}
};

node mrg(const node &a, const node &b) {
    node res;
    res.sum = a.sum + b.sum;
    res.mxp = std::max(a.mxp, a.sum + b.mxp);
    res.mxs = std::max(b.mxs, b.sum + a.mxs);
    res.mnp = std::min(a.mnp, a.sum + b.mnp);
    res.mns = std::min(b.mns, b.sum + a.mns);
    return res;
}